package game;

/**
 * Represents a 2D location or dimension.
 */
public class Point {
	public int x;
	public int y;

	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.x;
	}
}
